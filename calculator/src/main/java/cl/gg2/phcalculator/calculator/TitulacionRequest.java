package cl.gg2.phcalculator.calculator;

public class TitulacionRequest {
	private double volumenAcido;
	private double concentracionAcido;
	private double volumenBase;
	private double concentracionBase;

	// Getters y setters
	public double getVolumenAcido() { return volumenAcido; }
	public void setVolumenAcido(double volumenAcido) { this.volumenAcido = volumenAcido; }

	public double getConcentracionAcido() { return concentracionAcido; }
	public void setConcentracionAcido(double concentracionAcido) { this.concentracionAcido = concentracionAcido; }

	public double getVolumenBase() { return volumenBase; }
	public void setVolumenBase(double volumenBase) { this.volumenBase = volumenBase; }

	public double getConcentracionBase() { return concentracionBase; }
	public void setConcentracionBase(double concentracionBase) { this.concentracionBase = concentracionBase; }
}
